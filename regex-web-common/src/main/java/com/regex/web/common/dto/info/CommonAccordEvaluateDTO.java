package com.regex.web.common.dto.info;

import java.io.Serializable;

/**
 * 
 * 〈一句话功能简述〉<br> 
 * 一般复合性评价
 *
 * @author admin
 * @see [相关类/方法]（可选）
 * @since [产品/模块版本] （可选）
 */
public class CommonAccordEvaluateDTO implements Serializable {
    /**
     */
    private static final long serialVersionUID = 8165221509069111547L;
    private Long id;
    private java.util.Date updateTime;
    private java.util.Date createTime;
    private String isDel;
    private String evaluateType;//评价类型 0 绿色工厂评价 1 供应链企业评价 2  园区评价
    private String content;//一般要求
    public CommonAccordEvaluateDTO() {
        super();
    }
    public CommonAccordEvaluateDTO(Long id,java.util.Date updateTime,java.util.Date createTime,String isDel,String evaluateType,String content) {
        super();
        this.id = id;
        this.updateTime = updateTime;
        this.createTime = createTime;
        this.isDel = isDel;
        this.evaluateType = evaluateType;
        this.content = content;
    }
    public Long getId() {
        return this.id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public java.util.Date getUpdateTime() {
        return this.updateTime;
    }

    public void setUpdateTime(java.util.Date updateTime) {
        this.updateTime = updateTime;
    }

    public java.util.Date getCreateTime() {
        return this.createTime;
    }

    public void setCreateTime(java.util.Date createTime) {
        this.createTime = createTime;
    }

    public String getIsDel() {
        return this.isDel;
    }

    public void setIsDel(String isDel) {
        this.isDel = isDel;
    }

    public String getEvaluateType() {
        return this.evaluateType;
    }

    public void setEvaluateType(String evaluateType) {
        this.evaluateType = evaluateType;
    }

    public String getContent() {
        return this.content;
    }

    public void setContent(String content) {
        this.content = content;
    }

}
