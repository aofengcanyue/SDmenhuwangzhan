package com.regex.web.common.dto.sys;
public class SysFhbutton {
    private String fhbuttonId;
    private String qxName;//权限标识
    private String bz;//备注
    private String name;//名称
    public SysFhbutton() {
        super();
    }
    public SysFhbutton(String fhbuttonId,String qxName,String bz,String name) {
        super();
        this.fhbuttonId = fhbuttonId;
        this.qxName = qxName;
        this.bz = bz;
        this.name = name;
    }
    public String getFhbuttonId() {
        return this.fhbuttonId;
    }

    public void setFhbuttonId(String fhbuttonId) {
        this.fhbuttonId = fhbuttonId;
    }

    public String getQxName() {
        return this.qxName;
    }

    public void setQxName(String qxName) {
        this.qxName = qxName;
    }

    public String getBz() {
        return this.bz;
    }

    public void setBz(String bz) {
        this.bz = bz;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
